# Задача 3. Создайте словарь со списком вещей для похода в качестве ключа и их массой в качестве значения.
# Определите какие вещи влезут в рюкзак передав его максимальную грузоподъёмность.
# Достаточно вернуть один допустимый вариант.
# *Верните все возможные варианты комплектации рюкзака.

MAX_WEIGHT = 10
my_trip = {'Палатка': 3, 'Посуда': 1, 'Вода': 3, 'Еда': 4, 'Одежда': 2}

possible_goods = []
for item, weight in my_trip.items():
        if weight <= MAX_WEIGHT:
            possible_goods.append(item)
            MAX_WEIGHT -= weight
print(possible_goods)
