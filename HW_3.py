# Задача 3. В большой текстовой строке подсчитать количество встречаемых слов и вернуть 10 самых частых.
# Не учитывать знаки препинания и регистр символов.
# За основу возьмите любую статью из википедии или из документации к языку.

string = ('В декабре 1603 года султаном стал старший единокровный брат Мустафы Ахмед I. '
          'Сам Мустафа должен был быть казнён по закону Фатиха, однако по ряду причин '
          '(в частности, учитывая его психическое состояние) ему сохранили жизнь. '
          'Когда в ноябре 1617 года неожиданно умер Ахмед I, встал вопрос, кто унаследует трон: '
          'брат почившего султана Мустафа или его малолетний сын Осман. '
          'Чаша весов склонилась в сторону Мустафы и 22 ноября 1617 года его возвели на престол. '
          'Поскольку сам султан не был способен править, большую часть его обязанностей возложили на его мать. '
          'О действиях самого Мустафы в период его правления известно мало. '
          '26 февраля 1618 года с согласия матери султана Мустафа был свергнут '
          'и заточён во внутренних покоях дворца, '
          'где оставался на протяжении недолгого правления его племянника Османа II.')

